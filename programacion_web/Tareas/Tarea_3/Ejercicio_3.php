/*
-Asegúrate de tener XAMPP instalado:
Descarga e instala XAMPP desde el sitio oficial: https://www.apachefriends.org/index.html.

-Configura Apache para permitir acceso remoto:
Abre el archivo httpd-xampp.conf ubicado en la carpeta apache\conf\extra dentro de la instalación de XAMPP.
Busca la línea Require local y cámbiala a Require all granted.
Guarda el archivo.

-Configura el Firewall:
Asegúrate de que el firewall de tu computadora permita el tráfico a través del puerto que
Apache está utilizando (por defecto, es el puerto 80).

-Obtén la dirección IP de tu computadora:
Ejecuta ipconfig en la línea de comandos (en Windows) o ifconfig (en Linux/Mac). Busca la dirección IPv4.

-Prueba el acceso desde otra computadora:
Desde otra computadora en la misma red, abre un navegador y escribe la dirección IP de tu computadora 
seguida por el puerto de Apache (por ejemplo, http://192.168.1.2).
*/


